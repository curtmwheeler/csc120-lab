/**
 * Draws ASCII-art letters 'M' and 'S' to the console.
 */
public class Shapes {

    /**
     * Entry point: prints ASCII representations of the letters M and S.
     *
     * @param args command-line arguments (unused).
     */
    public static void main(String[] args) {
        // Letter 'M'
        System.out.println("____ This is an M ___");
        System.out.println("|\\       /|");
        System.out.println("| \\     / |");
        System.out.println("|  \\   /  |");
        System.out.println("|   \\ /   |");
        System.out.println("|          |");

        // Letter 'S'
        System.out.println("\n____ This is an S ___");
        System.out.println("+---------");
        System.out.println("|");
        System.out.println("|");
        System.out.println("+--------+");
        System.out.println("         |");
        System.out.println("         |");
        System.out.println("---------+");
    }
}
